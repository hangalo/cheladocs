package cheladocs.modelo;

/**
 *
 * @author Adelino Eduardo
 */
public class Endereco {
    private int idEndereco;
    private String caixaPostal;
    private String ruaEndereco;
    private String casaEndereco;
    private String bairroEndereco;
    private String cidadeEndereco;
    private Requerente requerente;
    private int flagActivo;
    private Municipio municipio;

    public int getIdEndereco() {
        return idEndereco;
    }

    public void setIdEndereco(int idEndereco) {
        this.idEndereco = idEndereco;
    }

    public String getCaixaPostal() {
        return caixaPostal;
    }

    public void setCaixaPostal(String caixaPostal) {
        this.caixaPostal = caixaPostal;
    }

    public String getRuaEndereco() {
        return ruaEndereco;
    }

    public void setRuaEndereco(String ruaEndereco) {
        this.ruaEndereco = ruaEndereco;
    }

    public String getCasaEndereco() {
        return casaEndereco;
    }

    public void setCasaEndereco(String casaEndereco) {
        this.casaEndereco = casaEndereco;
    }

    public String getBairroEndereco() {
        return bairroEndereco;
    }

    public void setBairroEndereco(String bairroEndereco) {
        this.bairroEndereco = bairroEndereco;
    }

    public String getCidadeEndereco() {
        return cidadeEndereco;
    }

    public void setCidadeEndereco(String cidadeEndereco) {
        this.cidadeEndereco = cidadeEndereco;
    }

    public Requerente getRequerente() {
        return requerente;
    }

    public void setRequerente(Requerente requerente) {
        this.requerente = requerente;
    }

    public int getFlagActivo() {
        return flagActivo;
    }

    public void setFlagActivo(int flagActivo) {
        this.flagActivo = flagActivo;
    }

    public Municipio getMunicipio() {
        return municipio;
    }

    public void setMunicipio(Municipio municipio) {
        this.municipio = municipio;
    }
    
}
